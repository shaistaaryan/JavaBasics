
package com.class01;

public class PracticeVariables {

	public static void main(String[] args) {

		/*
		 * Create a Java program and name it Practice Variables
         In your program create variables to store all different types
          of primitive data to test size range
         Print the value of each variable
		 */
		
		byte bt = 127;
		short a1 = 1234;
		int c2 = 3000;
		long d = 4000000000000L;

		float ft = 13.33f;
		char d1 = 'm';
		boolean bl = true;

		System.out.println(bt);
		System.out.println(a1);
		System.out.println(c2);
		System.out.println(d);

		System.out.println(ft);
		System.out.println(d1);
		System.out.println(bl);

	}

}
