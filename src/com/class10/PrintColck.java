package com.class10;

public class PrintColck {//ForLoops1 

	public static void main(String[] args) {
		
		 

			
				//using nested loop to print clock
				
				for ( int h = 0; h < 24 ; h++) {
					for(int m = 0; m < 60 ; m++) {
						
						if(m<10) {
							System.out.println(h+":"+ m);
						}else {
							System.out.println(h+":"+ m);
						}
						
						System.out.println(h+":"+ m);
					}
				}
			
			
				
			
			}
		


	}


